function plural(n: number): number {
  if (n === 0) return 0;
  if (n === 1) return 1;
  if (n === 2) return 2;
  if (n % 100 >= 3 && n % 100 <= 10) return 3;
  if (n % 100 >= 11) return 4;
  return 5;
}